// Action creator for requesting help.
// ----------------------------------------------------------------------------------------------------------------------------
    export const requestHelp = (details) =>
    {
        return{
            type: "REQUEST_HELP",
            payload: details,
        };
    };
// ----------------------------------------------------------------------------------------------------------------------------

// Action creator for closing help request.
// ----------------------------------------------------------------------------------------------------------------------------
    export const closeHelp = () =>
    {
        return{
        type: "CLOSE_HELP",
        };
    };
// ----------------------------------------------------------------------------------------------------------------------------
